			INW mapPtr			; Pointer for comparison to next atom
			LDR mapPtr			; next atom
			CPR	vaddr			; compare next atom
			BEQ	chkGaOv4		; still agreement
			INW arrayPtr		; No agreement Reset comparison pointer
			LDI <chkList
			STB mapPtr+0
			LDI >chkList
			STB mapPtr+1
