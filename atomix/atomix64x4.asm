			STB cursorX
			LDI <_cursor
			STB cursorId
			; first cursor character
			LDA cursorX
			STB X8
			LDA cursorY
			STB Y8
			LDA cursorId
			STB spriteId
			JPS calcXY
			JPS plotSprite16x16
