			CPI 0x00				; Is the target field free?
			BEQ moveAtom1			; yes, then move the atom
			LDA X8					; no, then restore old coordinates
			STB cursorX
			LDA Y8
			STB cursorY
			JPS chkGameOver
			JPA gameLoop			; Move atom finished
