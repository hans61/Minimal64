			LDI 14+29		; To the next line
			ADW ldst		
			DEZ <rY
			BGT lyloop		; Logo not yet ready Branch on Greater (>)
			
			JPS loadLevel	; Initialize the game map for the respective level
			JPS printMolecule	; Target sign
