; PHS Char
; outputs a character at the position _XPos(0..39), _YPos (0..23) grid 10x10 pixel
; _XPos and _YPos are updated, there is no scrolling
printCharXY:	
			CLB	xPos16+0x01
			LDA _XPos
			LSL
			STB xPos16+0x00 ; xPos16 = _XPos * 2
			PHS				; memorize _XPos * 2
			LLW	xPos16		; xPos16 = _XPos * 4
			LLW	xPos16		; xPos16 = _XPos * 8
			PLS				; Restore _XPos * 2
			ADW xPos16		; xPos16 = _XPos * 8 + _XPos * 2 = _XPos * 10
			LDA xPos16+0x00
			PHS				; XPos low
			LDA xPos16+0x01
			PHS				; XPos hight
			LDA _YPos
			LSL
			STB yPos8
			L2L
			ADA yPos8
			PHS				; yPos = _YPos* 10
			LDS 6			; Char is LDS 3+3
			PHS
			JPS printChar
			PLS PLS PLS PLS
			INB _XPos
			LDA _XPos
			CPI 40
			BCC pCxy1
			CLB _XPos
			INB _YPos
			LDA _YPos
			CPI 24
			BCC pCxy1
			CLB _YPos
pCxy1:		RTS
xPos16:		0x00, 0x00,
yPos8:		0x00,
; ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; print char 0x20..0x5f
; PHS: xLow, xHiht, y, char
printChar:		LDS 4               	; Y
                L6L STB addr+0      	;
                LDS 4               	; Y
                R7L ANI 63 ADI 0x43 STB addr+1
                LDS 5               	; Xhight
                DEC 
                LDS 6               	; Xlow
                R6L ANI 63 ADI 12 ORB addr+0
                LDS 6 ANI 7 STB shift	; Xlow
                LDI 10 STB scnt			; scnt Loop counter 8 bytes + 2
                LDS 3					; char
				CPI 0x60
				BCS rPCrts				; char > 0x5f
				SBI 0x20
				BCC rPCrts				; char < 0x20
				STB spritePointer+0
				CLB spritePointer+1		; spritePointer=(char-' ')
				LLW spritePointer		; *2
				LLW spritePointer		; *4
				LLW spritePointer		; *8 8 bytes per character
				LDI <alphaNumSprites	; Sprite data low
				ADW spritePointer		; Add to pointer
				LDI >alphaNumSprites
				ADB spritePointer+1
				DEW spritePointer
clineloop:      LDA scnt
				CPI 10
				BEQ cl1
				CPI 1
				BEQ cl1
				INW spritePointer
				LDR spritePointer
				JPA cl2
cl1:			LDI 0x00
cl2:            STB buffer+0        
                CLB buffer+1
                CLB buffer+2
                CLB mask+0
				LDI 0xfc
				STB mask+1
				LDI 0xff
				STB mask+2
                LDA shift STZ <rY      		; shift counter     
				DEZ <rY                 
                BCC cshiftdone 
cshiftloop:  	LLW buffer+0        ; logical shift to the left word absolute address
				RLB buffer+2        ; rotate shift left byte absolute address
				SEC
				RLW mask+0
				RLB mask+2
				DEZ <rY                 ; decrement X
				BCS cshiftloop		; branch on carry Set
cshiftdone:		LDA mask+0
				ANR addr 
                STR addr            
                LDA buffer+0
				ORR addr            
                STR addr            
                INW addr            
                LDA mask+1        
				ANR addr 
                STR addr            
                LDA buffer+1        
                ORR addr            
                STR addr            
                INW addr            
                LDA mask+2
				ANR addr 
                STR addr            
                LDA buffer+2
                ORR addr            
                STR addr            
ccommon:        LDI 62              
                ADW addr            
                DEB scnt
                BNE clineloop		
rPCrts:         RTS
_tmp:			0x00,0x00,
scnt:           0x00,
shift:          0xff,
addr:           0xffff,
;buffer:         0xff, 0xff, 0xff,
mask:			0xff, 0xff, 0xff,
spritePointer:	0xffff,
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; Calculates the pixel coordinates posX,posY from the raster coordinates X8,Y8
calcXY:		LDA X8
			L3L
			STB posX+0
			CLB posX+1
			LLW posX
			LDA Y8
			L4L
			STB posY
			RTS
			
; spriteId ist die Nummer des Sprites, posX ist 16-bit xpos, posY ist ypos
; Draw sprite
plotSprite16x16:
			LDA posX+0 ;m70
			ANI 7
			STB _shift			;x mod 8 = shift
			LDA posY            ; Lade A vom Stack Offset 3 (Y Position)
			L6L                 ; Logical Shift Left 6 A
			STB vaddr+0         ; Store A to low Byte Y * 64    ; use ypos
			LDA posY            ; Lade A vom Stack Offset 3
			R7L                 ; Rotate Shift Left 7 (RL7 1 0C765432)
			ANI 63              ; Bitwise AND (3F)
			ADI 0x43            ; Add       (0b11000011) ADD A=A+C3
			STB vaddr+1         ; Store A to high Byte Y
			; X Postion 0..399
			LDA posX+1          ; Lade A vom Stack Offset 4 Xhigh -> beeinflusst C nicht
			DEC                 ; Decrement A -> dabei wird C gesetzt
			LDA posX+0          ; Lade A vom Stack Offset 5 Xlow                    ; add xpos
			R6L                 ; Rotate Shift Left 6 -> RL6 = 2 10C76543 -> = X/8
			ANI 63              ; Bitwise AND 0x3F
			ADI 12              ; Add 0x0C
			ORB vaddr+0         ; Bitwise OR ; preprare target address
			; set sprite
			LDA spriteId
			STB spriteAddr+0
			CLB spriteAddr+1
			LLW spriteAddr		; *2
			LLW spriteAddr		; *4
			LLW spriteAddr		; *8
			LLW spriteAddr		; *16
			LLW spriteAddr		; *32
			LDI <spriteData
			ADW spriteAddr
			LDI >spriteData
			ADB spriteAddr+1
			LDI 16
			STB _sloop
            ; Load sprite data into buffer for moving
lineloop:   LDR spriteAddr		; Pointer auf Daten
            STB buffer+0        ; niedigstes Byte
            INW spriteAddr      ; Pointer+1
            LDR spriteAddr      ; Pointer auf nÃ¤chstes Byte
            STB buffer+1        ; high Byte Sprite (rechte Seite)
            CLB buffer+2        ; Clear Byte (drittes Buffer-Byte 0 fÃ¼r Pixelverschiebung)

            LDA _shift STZ <rX          ; Lade X von absoluter Adresse (Verschiebe-Offset)
            DEZ <rX                 ; Decrement X
            BCC shiftdone       ; Branch on Carry Clear     ; shift that buffer to pixel position (kein verschieben nÃ¶tig C=0 wenn shift=0)
shiftloop:  LLW buffer+0        ; Logical Shift Left Word absolute Adresse
            RLB buffer+2        ; Rotate Shift Left Byte absolute Adresse
            DEZ <rX                 ; Decrement X
            BCS shiftloop       ; Branch on Carry Set
