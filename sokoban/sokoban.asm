#org 0xc30c ViewPort:

#org 0x2000
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ Start Game ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
begin:		LDI 0xfe STA 0xffff ; SP initialize
startGame:  LDI 0x01
			STA level
			LDI 0x0c
			STA dst1+0
			LDI >ViewPort
			STA dst1+1
            LDI <pic
			STA src1+0
			LDI >pic
			STA src1+1
            LYI 240
yloop:      LXI 50
xloop:      LDA
src1:		0xffff 
			STA
dst1:		0xffff
			INW src1
			INW dst1
			DEX
			BGT xloop
			LDI 14
			ADW dst1
			DEY
			BGT yloop
            JPS _WaitInput

nextLevel:	JPS _Clear          ; Clear screen
			CLB _XPos
			CLB _YPos
			CLB backMoveEnd
			CLB backMoveRdWr
			JPS buildTileMap
			JPS printInfo
			JPS printMoves
			JPS printTileMap
			JPS printPlayer
			
gameLoop:	
			CLB dX
			CLB dY
			LDA allTargets
			CPA cntTargets
			BEQ levelDone
			LDI <backMove
			STA backMovePtr+0
			LDI >backMove
			STA backMovePtr+1
			LDA backMoveRdWr
			ADW backMovePtr
			JPS _WaitInput		; keyboard input
			CPI 'n'
			BEQ nextLevel
			CPI 'N'
			BEQ nextLevel
			CPI 'g'
			BEQ readLevel
			CPI 'G'
			BEQ readLevel
			CPI 'r'
			BEQ playerMoveBack
			CPI 'R'
			BEQ playerMoveBack
			CPI 0xe7			; PgUp
			BEQ incLevel
			CPI 0x2b			; +
			BEQ incLevel
			CPI 0xe8			; PgDn
			BEQ decLevel
			CPI 0x2d			; -
			BEQ decLevel
			CPI 0xe1
			BEQ key_up
			CPI 0xe2
			BEQ key_down
			CPI 0xe3
			BEQ key_left
			CPI 0xe4
			BEQ key_right
			JPA gameLoop
key_up:		LDI 0xff
			STA dY
			LDI 0x03
			STR backMovePtr
			JPA checkMove
key_down:	LDI 0x01
			STA dY
			LDI 0x01
			STR backMovePtr
			JPA checkMove
key_right:	LDI 0x01
			STA dX
			LDI 0x00
			STR backMovePtr
			JPA checkMove
key_left:	LDI 0xff
			STA dX
			LDI 0x02
			STR backMovePtr
checkMove:	LDA xPosPlayer			; 0..17
			ADA dX
			TAX
			LDA yPosPlayer			; 0..21
			ADA dY
			TAY
			JPS calcMapPtr
			LDR tmpPtr
			CPI <_brick				; brick
			BEQ gameLoop
			CPI <_box				; box
			BEQ checkBox
			CPI <_boxtarget			; box in target
			BEQ checkBox
movePlayer:	JPS incMoves
			LXA xPosPlayer			; 0..17
			LYA yPosPlayer			; 0..21
			JPS calcMapPtr
			JPS printPlayer			; delete player sprite
			JPS setPlayerSptite
			INB backMoveRdWr			; Player was moved Move writing pointer forward
			LDI 0x0f				; Write pointer values 0..15
			ANB backMoveRdWr
			CPA backMoveEnd
			BNE moveP1
			INB backMoveEnd			; Ring buffer is full, move end pointer
			LDI 0x0f
			ANB backMoveEnd
moveP1:		LDA xPosPlayer			; 0..17
			ADA dX
			STA xPosPlayer
			TAX
			LDA yPosPlayer			; 0..21
			ADA dY
			STA yPosPlayer
			TAY
			JPS calcMapPtr
			JPS printPlayer			; player
			JPA gameLoop
checkBox:							; Box or box at the finish -> check if there is space behind it
			LDA xPosPlayer			; 0..17
			ADA dX
			ADA dX
			STA xPos
			TAX
			LDA yPosPlayer			; 0..21
			ADA dY
			ADA dY
			STA yPos
			TAY
			JPS calcMapPtr			; points to the target of the box
			LDR tmpPtr
			CPI <_space				; target field behind box is a free field
			BEQ moveBox
			CPI <_target			; target field behind box is a free target
			BEQ moveBox1
			JPA gameLoop
moveBox:	LDI	<_box				; change box in destination to map
			STR tmpPtr
			JPA moveBox2
moveBox1:	INB cntTargets			; free target -> move ok -> box is in destination increase counter
			LDI	<_boxtarget			; box on target set in map
			STR tmpPtr
moveBox2:	JPS incPushes
			JPS printBox			; paint box in the new target
			LDA xPosPlayer			; 0..20
			ADA dX
			STA xPos
			TAX
			LDA yPosPlayer			; 0..16
			ADA dY
			STA yPos
			TAY
			JPS calcMapPtr
			LDR tmpPtr				; old field of the box
			CPI <_boxtarget			; box on target
			BEQ moveBox3
			LDI <_space				; empty field
			JPA moveBox4
moveBox3:	DEB cntTargets
			LDI <_target			; box at the finish
moveBox4:	STR tmpPtr
			JPS printBox			; delete old box
			LDI 0x80				; mark box with moves
			ORR backMovePtr
			STR backMovePtr
			JPA movePlayer


levelDone:	
			LDI 10 STA _YPos
			LDI 1 STA _XPos
			JPS printLine ' @_________________________________@ ', 0
			LDI 11 STA _YPos
			LDI 1 STA _XPos
			JPS printLine ' @ LEVEL FINISHED PRESS ANY BUTTON @ ', 0
			LDI 12 STA _YPos
			LDI 1 STA _XPos
			JPS printLine ' @_________________________________@ ', 0
			JPS _WaitInput
incLevel:	LDA level
			INC
			CPI 91					; max level 90
			BCC levelD1
			LDI 1					; first level
levelD1:	STA level
			JPA nextLevel
decLevel:	LDA level
			DEC
			BNE levelD1
			LDI 90					; last level
			JPA levelD1

playerMoveBack:
			LDA backMoveEnd
			CPA backMoveRdWr
			BEQ finMB				; no undo information available anymore
			LDI <backMove			; Set pointer for reading
			STA backMovePtr+0
			LDI >backMove
			STA backMovePtr+1
			DEB backMoveRdWr
			LDI 0x0f
			ANB backMoveRdWr
			ADW backMovePtr
			CLB dX
			CLB dY
			LDR backMovePtr
			ANI 0x03				; two bit mask for direction of movement
			CPI 1
			BNE playerMB1
			LDI 0xff				; down
			STA dY
			JPA playerMB4
playerMB1:	CPI 2
			BNE playerMB2
			LDI 0x01				; left
			STA dX
			JPA playerMB4
playerMB2:	CPI 3
			BNE playerMB3
			LDI 0x01				; up
			STA dY
			JPA playerMB4
playerMB3:	LDI 0xff				; right
			STA dX
playerMB4:	JPS printPlayer			; delete player sprite
			LDR backMovePtr			; undo data
			ANI 0x80
			CPI 0x00
			BEQ noMBack				; there is no need to move a box
			JPS incPushes
			LYA yPosPlayer			; box must be moved to the old player position
			LXA xPosPlayer
			JPS calcMapPtr
			LDR tmpPtr
			CPI <_target			; is this a target
			BNE playerMB5			; field is not a target so only box
			INB cntTargets			; new position is a target
			LDI <_boxtarget			; field was target now box in target
			JPA playerMB6
playerMB5:	LDI <_box				; box
playerMB6:	STR tmpPtr
			LDA xPosPlayer
			STA xPos
			LDA yPosPlayer
			STA yPos
			JPS printBox			; print new box
			LDA xPosPlayer
			SBA dX
			STA xPos
			LDA yPosPlayer
			SBA dY
			STA yPos
			JPS printBox			; delete old box
			LDA xPos
			TAX
			LDA yPos
			TAY
			JPS calcMapPtr
			LDR tmpPtr
			CPI <_boxtarget			; was the box previously on a target
			BNE playerMB7
			DEB cntTargets
			LDI <_target
			JPA playerMB8
playerMB7:	LDI <_space
playerMB8:	STR tmpPtr
noMBack:	LDA xPosPlayer			; 0..20
			ADA dX
			STA xPosPlayer
			LDA yPosPlayer			; 0..16
			ADA dY
			STA yPosPlayer
			JPS calcMapPtr
			JPS printPlayer
finMB:		LDI 0x00
			STR backMovePtr
			JPS incMoves
			JPA gameLoop
; ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; output regA as a three-digit decimal number
printThreeDigitNumber:
			LDI 100
			STA tmp03				; divider
			LDI 0x02
			STA tmp02				; decimal place
			LDA regA
nextDigit:
			CLB tmp04				; place counter
countUnits:
			PHS INB tmp04 PLS
			SBA tmp03				; A = A - [100|10]
			BCS countUnits
			ADA tmp03				; A = A + [100|10]
			PHS						; remember A, A = A/[100|10]
			LDA tmp04
			ADI 0x2f
			PHS JPS printCharXY PLS
			LDI 10
			STA tmp03
			DEB tmp02				; loop counter three digits (changed a)
			PLS						; get memorized A
			BNE nextDigit			; digits ready?
			ADI 0x30
			PHS JPS printCharXY PLS
			RTS
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; increment cntMoves (word)
incMoves:	CLB bcdC
			LDI 0x01
			STA sum1
			LDA cntMoves+0
			STA sum2
			JPS bcdAdd
			LDA result
			STA cntMoves+0
			CLB sum1
			LDA cntMoves+1
			STA sum2
			JPS bcdAdd
			LDA result
			STA cntMoves+1
			JPA printMoves
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; increment cntMoves (word)
incPushes:	CLB bcdC
			LDI 0x01
			STA sum1
			LDA cntPushes+0
			STA sum2
			JPS bcdAdd
			LDA result
			STA cntPushes+0
			CLB sum1
			LDA cntPushes+1
			STA sum2
			JPS bcdAdd
			LDA result
			STA cntPushes+1
			JPA printPushes
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; bcd for large numbers for faster printing
; add bcd -> result = sum1 + sum1 carry in bcdC
bcdAdd:		;CLB bcdC				; carry = 0 must be set at the first call in the calling program
			JPS bcdNib				; add lower nibble
			LDA tmp1				; result in tmp1
			STA result				; memorize
			LDA sum1
			RL5
			STA sum1
			LDA sum2
			RL5
			STA sum2
			JPS bcdNib
			LDA tmp1
			LL4
			ORB result
			RTS
bcdNib:		LDA sum1				; 1. summand
			ANI 0x0f				; lower nibble
			ADB bcdC				; Add carry 0|1 
			STA tmp1				; save result
			CLB bcdC				; clear carry
			LDA sum2				; 2. summand
			ANI 0x0f				; lower nibble
			ADB tmp1				; add -> A, tmp1 = sum1+sum2+carry
			CPI	9
			BLE done				; if <= 9 ready (0..9)
			SBI 10					; result = result - 1
			STA tmp1
			INB bcdC				; carry = 1
done:		RTS

result:		0x00,
tmp1:		0x00,
bcdC:		0x00,
sum1:		0x00,
sum2:		0x00,
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; sets player sprite number by direction of movement
setPlayerSptite:
			LDA dY
			CPI 0xff
			BNE setPS1
			; down
			LDI 0x07
			JPA setPS4
setPS1:		CPI 0x01
			BNE setPS2
			; up
			LDI 0x08
			JPA setPS4
setPS2:		LDA dX
			CPI 0x01
			BNE setPS3
			; right
			LDI 0x05
			JPA setPS4
setPS3:		; left
			LDI 0x06
setPS4:		STA playerSptite
			TXA
			RTS
; ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; calculates the pointer in the game map for a position x(0..20),y(0..16)
; input: x,y output tmpPtr (pointer in the map)
calcMapPtr:
			LDI	<tileMap
			STA tmpPtr+0x00
			LDI	>tileMap
			STA tmpPtr+0x01
			INY
calcMP1:	DEY
			BEQ calcMP2
			LDI 21
			ADW tmpPtr
			JPA calcMP1
calcMP2:	TXA
			ADW tmpPtr					; tmpPtr player address in map
			RTS
; ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; print player from the position xPosPlayer,yPosPlayer
printPlayer:
			LDA xPosPlayer
			STA regA
			JPS times14_16bit
			LDA yPosPlayer
			STA regA
			JPS times14
			LDA playerSptite					
			STA regA
			JPA plotSprite14x14
; ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; print boc from the position xPos,yPos
printBox:
			LDA xPos
			STA regA
			JPS times14_16bit
			LDA yPos
			STA regA
			JPS times14
			LDI 0x02
			STA regA
			JPA plotSprite14x14
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; input: regA, output: 14*regA in posX16 (16*A - 2*A)
times14_16bit:
			CLB posX16+0x01
			LLB regA	; regA = 2*regA
			LDA regA
			STA posX16+0x00
			LLW	posX16		; * 4
			LLW	posX16		; * 8
			LLW	posX16		; * 16
			LDA regA
			SBW posX16
			RTS

; input: regA, output 10*A in regY & Y
times14:
			LLB regA	; regA = 2*regA
			LDA regA
			STA posY8
			LLB posY8
			LLB posY8
			LLB posY8
			LDA regA
			SBB posY8
			LDA posY8
			STA regY
			TAY
			RTS
posX16:		0xffff,
posY8:		0x00,
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; regA is sprite number, posX16 16-bit xpos, Y is ypos
; ##### draw sprite
plotSprite14x14: ;pha
			LDA posX16+0
			ANI 7
			STA tmp00			;x mod 8 = shift
			JPS calcScreenAddr
			; set sprite
			LDA regA
			LSL					; A = 2 * A
			TAX					; X = A
			LTX spritetmpPtr16
			STA spAdr+0			;sprite tmpPtr lo
			INX
			LTX spritetmpPtr16
			STA spAdr+1			;sprite tmpPtr hi
			LDI 14
			STA spCnt
            ; Load sprite data into buffer for moving
lineloop:   LDR spAdr+0			; pointer to data
            STA buffer+0		; lowest byte
            INW spAdr+0			; pointer+1
            LDR spAdr+0			; pointer to next byte
            STA buffer+1		; high byte sprite (right side)
            CLB buffer+2		; clear byte (third buffer byte 0 for pixel shift)
            LXA tmp00           ; load X from absolute address (shift offset)
            DEX                 ; decrement X
            BCC shiftdone       ; Branch on Carry Clear     ; shift that buffer to pixel position (no shift necessary C=0 if shift=0)
shiftloop:  LLW buffer+0        ; logical shift to the left word absolute address
            RLB buffer+2        ; rotate shift left byte absolute address
            DEX                 ; decrement X
            BCS shiftloop       ; branch on carry Set
shiftdone:
			LDR posX16+0
			XRA buffer+0
			STR posX16+0
			INW posX16+0
			LDR posX16+0
			XRA buffer+1
			STR posX16+0
			INW posX16+0
			LDR posX16+0
			XRA buffer+2
			STR posX16+0
			LDI 62              ; A = 62
			ADW posX16+0		; VRAM + 62     ; ... and move to the next line
			INW spAdr+0			; next sprite data byte
			LDA spAdr+0
			DEB spCnt
			BNE lineloop        ; we have processed all sprite data?
			RTS
spCnt:		0x00,
buffer:		0xff, 0xff, 0xff,	; shift buffer
; ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; calculate address in video memory
calcScreenAddr:
			; Y postion 0..239
			TYA		            ; Load A from stack offset 3 (Y position)
			LL6                 ; Logical Shift Left 6 A
			STA vAdr+0          ; Store A to low Byte Y * 64    ; use ypos
			TYA		            ; Lade A vom Stack Offset 3
			RL7                 ; Rotate Shift Left 7 (RL7 1 0C765432)
			ANI 63              ; Bitwise AND (3F)
			ADI >ViewPort       ; Add       (0b11000011) ADD A=A+C3
			STA vAdr+1          ; Store A to high Byte Y
			; X postion 0..399
			LDA posX16+1		; Load A from stack Offset 4 Xhigh -> does not affect C
			DEC                 ; Decrement A -> C is adjusted in the process
			LDA posX16+0		; Load A from stack Offset 5 Xlow                    ; add xpos
			RL6                 ; Rotate Shift Left 6
			ANI 63              ; Bitwise AND 0x3F
			ADI 12              ; Add 0x0C
			ORB vAdr+0          ; Bitwise OR ; preprare target tmpPtress
			LDA vAdr+0
			LDA vAdr+0
			LDA vAdr+0
			STA posX16+0
			LDA vAdr+1
			STA posX16+1
			RTS
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; print playing field
printTileMap:
			LDI	<tileMap		; tmpPtr Pointer to current level data (nibble)
			STA tmpPtr+0x00
			LDI	>tileMap
			STA tmpPtr+0x01		; tmpPtr Pointer to the destination of the byte-oriented data
			CLB xPos
			CLB yPos
lineLoop:	LDA xPos
			STA regA
			JPS times14_16bit
			LDA yPos
			STA regA
			JPS times14
			LDR tmpPtr					
			STA regA
			LDA regA
			CPI 0x00
			BEQ nextTile
			JPS plotSprite14x14
nextTile:	INW tmpPtr
			INB xPos
			LDA xPos
			CPI 21
			BNE lineLoop
			CLB xPos
			INB yPos
			LDA yPos
			CPI 17
			BNE lineLoop
			RTS
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; reads the current level into the parts array tileMap (sprite numbers)
; tileMap is an array of 21 columns and 17 rows
; level ab levelData in compressed form
; byte 1 is number of data bytes
; byte 2 is x position of the player
; byte 3 is y position of the player
; byte 4 is number of the level
; byte 5..n data bytes
; data coded in 2 bit pairs
; 1. bit pairs
; 0=nothing to fill up bytes 1=empty field 2=brick wall 3=another bit couple follows
; 2. bit pairs
; 0=box 1=target 2=box on target 3=end of one line to the beginning of the next line
buildTileMap:
			LDI	<tileMap		; Delete old map
			STA tmpPtr+0x00
			LDI	>tileMap
			STA tmpPtr+0x01
			CLB allTargets
			CLB cntTargets
			CLW cntMoves
			CLW cntPushes
			LXI 0x00
buildTM1:	LDI 0x00
			STR tmpPtr
			INW tmpPtr
			DEX
			BNE buildTM1
			LXI 0x65
buildTM2:	LDI 0x00
			STR tmpPtr
			INW tmpPtr
			DEX
			BNE buildTM2
; Complete with map fill 0x00
			LDI	<tileMap		; tmpPtr Pointer to current level data (nibble)
			STA tmpPtr+0x00
			STA tmp00			; also used in plotSprite14x14
			LDI	>tileMap
			STA tmpPtr+0x01		; tmpPtr Pointer to the destination of the byte-oriented data
			STA tmp01
			
			LDI	<levelData
			STA dataPtr+0x00
			LDI	>levelData
			STA dataPtr+0x01	; dataPtr Pointer to the destination of the byte-oriented data
			LDA level
			CPI 91				; max level 90
			BMI buildTM3
			LDI 1
			STA level
buildTM3:	LXA	level			; X = (level) level number
buildTM4:	DEX					; Calculate pointers to level data
			BEQ bTMdataLoop
			LDI 105				; to the next level
			ADW dataPtr
			JPA buildTM4
			
bTMdataLoop:
			LDR dataPtr			; Pointer to level data -> load Header
			STA bytesCounter	; first byte number of data bytes
			DEB bytesCounter
			DEB bytesCounter
			INW dataPtr
			
			LDR dataPtr
			STA xPosPlayer
			INW dataPtr
			
			LDR dataPtr
			STA yPosPlayer
			INW dataPtr
			INW dataPtr			; Skip level number
			
			LXI 1				; X=1
buildTN3:	DEX					; DEC X = ZÃ¤hler auf 4 bit-Gruppen im Byte
			BNE buildTN4
			LDR dataPtr			; next byte of compressed data
			STA dataByte
			INW dataPtr
			DEB bytesCounter
			BEQ bTNrts
			LXI 4
buildTN4:	LDI 3
			ANA dataByte
			PHS
			LRB dataByte
			LRB dataByte
			PLS
			CPI 0x00
			BEQ buildTN3
			CPI 0x01
			BNE buildTN5
			LDI <_space
			JPA bTMstore
buildTN5:	CPI 0x02
			BNE buildTN6
			LDI <_brick
			JPA bTMstore
buildTN6:	DEX
			BNE buildTN7
			LDR dataPtr
			STA dataByte
			INW dataPtr
			DEB bytesCounter
			BEQ bTNrts
			LXI 4				; number of bit pairs per byte
buildTN7:	LDI 3				; mask two bit
			ANA dataByte
			PHS
			LRB dataByte
			LRB dataByte
			PLS
			CPI 0x00
			BNE buildTN8
			INB allTargets
			LDI <_box
			JPA bTMstore
buildTN8:	CPI 0x01
			BNE buildTN9
			LDI <_target
			JPA bTMstore
buildTN9:	CPI 0x02
			BNE buildTN10
			INB allTargets
			LDI <_boxtarget
bTMstore:		
			STR tmpPtr
			INW tmpPtr
			JPA buildTN3
buildTN10:	; End of the line to the beginning of the next	
			LDI 21				; map pointer to the beginning of the next line
			ADW tmp00
			LDA tmp00
			STA tmpPtr+0x00
			LDA tmp01
			STA tmpPtr+0x01
			JPA buildTN3
bTNrts:		RTS
dataPtr:	0xffff,
dataByte:	0x00,
bytesCounter: 0x00,
;~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
; output on the left edge of the screen
printInfo:	CLB _YPos
			LDI 29 STA _XPos
			JPS printLine 'SOKOBAN', 0
			
			LDI 2 STA _YPos
			LDI 29 STA _XPos
			JPS printLine 'LEVEL:', 0
			
			LDI 4 STA _YPos
			LDI 29 STA _XPos
			JPS printLine 'MOVES:', 0
			
			LDI 6 STA _YPos
			LDI 29 STA _XPos
			JPS printLine 'PUSHES:0000', 0

			LDI 8 STA _YPos
			LDI 29 STA _XPos
			JPS printLine '___________', 0
			
			LDI 10 STA _YPos
